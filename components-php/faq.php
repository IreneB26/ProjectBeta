<h1>FAQ</h1>

<div class="parent-container">
    <ul class="faq">
        <li>
            <h3 class="question">¿Qué sucede si mi evento es cancelado o reprogramado?
                <div class="plus-minus-toggle collapsed"></div>
            </h3>
            <div class="answer">
                <p>
                    Tan pronto como el lugar o el promotor nos informe que un evento ha sido cancelado o reprogramado, nos pondremos a trabajar para informarle al respecto. Si se cancela su evento, no tendrá que hacer nada, ya que le reembolsaremos su pedido automáticamente. Si su evento se reprograma, nos pondremos en contacto con usted con respecto a las nuevas fechas y si los boletos se transferirán.
                </p><br>


                <p>
                    Intentaremos comunicarnos con usted por mensaje de texto y correo electrónico, ¡así que asegúrese de que los detalles de su cuenta estén actualizados!
                </p>
            </div>
        </li>


        <li>
            <h3 class="question">¿Cómo obtengo un reembolso por mi reserva si se cancela mi evento?
                <div class="plus-minus-toggle collapsed"></div>
            </h3>
            <div class="answer">
                <p>
                    Si un evento se cancela por completo, generalmente solo reembolsaremos sus boletos automáticamente. Reembolsamos el valor nominal de cada boleto y verá un crédito en su tarjeta después de que le enviemos un correo electrónico sobre la cancelación con una escala de tiempo.
                </p>
            </div>
        </li>



        <li>
            <h3 class="question">Todavía no he recibido mi reembolso, ¿qué debo hacer?
                <div class="plus-minus-toggle collapsed"></div>
            </h3>
            <div class="answer">
                <p>
                    A veces puede tomar un tiempo para que un reembolso vuelva a aparecer en su tarjeta, ya que una vez que se procesa por nuestra parte, pueden pasar algunos días antes de que su banco lo reclame. Espere hasta 15 días después de que se haya procesado el reembolso para que aparezca. Si se cancela su evento, no tendrá que hacer nada, ya que le reembolsaremos su pedido automáticamente.
                </p><br>

                <p>
                    Si aún no ve el crédito en su estado de cuenta, vuelva a verificar qué tarjeta usó; siempre le reembolsaremos la tarjeta original con la que hizo la reserva.
                </p>
            </div>
        </li>
    </ul>
</div>